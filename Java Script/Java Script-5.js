 
 var num1=parseFloat(prompt("Enter your number"));

 if(num1 >=80 && num1 <=100)
 document.write(" Your result in= (A+)");
 
 else if (num1 >=70 && num1 <=79)
 document.write(" Your result in= (A)");
 
 else if (num1 >=60 && num1 <=69)
 document.write("  Your result in= (A-)");
 
 else  if (num1 >=50 && num1 <=59)
 document.write(" Your result in= (B)");
 
 else if (num1 >=40 && num1 <=49)
 document.write("  Your result in= (C)");
 
 else if (num1 >=33 && num1 <=39)
 document.write("  Your result in= (D)");
 
 else  
 document.write("  Your result in= (Failed)");
 